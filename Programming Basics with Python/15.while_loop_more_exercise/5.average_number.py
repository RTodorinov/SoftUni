''' Напишете програма, която прочита едно число n, след това прочита n на брой цели числа и принтира
средно аритметичното на тяхната сума число, форматирано до втората цифра след десетични знак. '''

n = int(input())  # прочитаме броя числа
# четем n на брой числа и ги сумираме
n_sum = 0
for i in range(n):
    number = int(input())
    n_sum += number
# изчисляваме средното аритметично и го принтираме
average = n_sum / n
print(f"{average:.2f}")
